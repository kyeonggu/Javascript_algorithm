/*
#===========================================================================================================================#
    자연수 N이 입력되면 1부터 N까지의 합을 출력하는 프로그램을 작성하세요. 
#===========================================================================================================================#
*/

/*
#===========================================================================================================================#
    입력 설명
    - 첫 번째 줄에 20이하의 자연수 N이 입력된다.
    
    출력 설명
    - 첫 번째 줄에 1부터 N까지의 합을 출력한다

    입력 예제 1
    6

    출력 예제 1
    21

    입력 예제 2
    10

    출력 예제 2
    55
#===========================================================================================================================#
*/

function solution(n){
    var answer = 0;

    for(var i=1; i<=n; i++) {
        answer += i;
    }

    return answer

    /*
    #===========================================================================================================================#
        1. answer값을 0으로 초기화한다. 이 작업을 하지 않을 경우 undefined로 남아서 숫자를 더해도 NaN이 나오게 된다.
        2. for 반복문을 통해서 1부터 n까지의 숫자를 반복해서 더해준다.
        3. answer에 +=로 i값을 계속 더해준다.
    #===========================================================================================================================#
    */
}

console.log(solution(10));