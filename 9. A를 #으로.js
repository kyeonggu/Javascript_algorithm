/*
#===========================================================================================================================#
    대문자로 이루어진 영어단어가 입력되면 단어에 포함된 ‘A'를 모두 ’#‘으로 바꾸어 출력하는 프로그램을 작성하세요.
#===========================================================================================================================#
*/

/*
#===========================================================================================================================#
    입력 설명
    - 첫 번째 줄에 문자열이 입력된다.
    
    출력 설명
    - 첫 번째 줄에 바뀐 단어를 출력한다.

    입력 예제 1
    BANANA

    출력 예제 1
    B#N#N#
#===========================================================================================================================#
*/

function solution(s){

    var answer = s;
    answer = s.replace(/A/g, "#");
    
    return answer;

    /*
    #===========================================================================================================================#
        1. 문자열을 파라미터 s로 설정해서 answer에 넣는다.
        2. replace로 해당 문자열의 //안에 있는 A를 찾아서 "#"로 바꿔준다.
        * //옆에 g를 붙이지 않으면 맨 첫번째 것만 변경되므로 global이라는 의미로 g를 넣어야 모든 문자열이 변경된다.
    #===========================================================================================================================#
    */
}

let str="BANANA";
console.log(solution(str));